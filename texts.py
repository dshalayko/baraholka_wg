def get_lang(obj):
    if obj is None:
        return "en"
    code = None
    if hasattr(obj, "effective_user"):
        code = getattr(obj.effective_user, "language_code", None)
    else:
        code = getattr(obj, "language_code", None)
    return "ru" if code and str(code).lower().startswith("ru") else "en"

